    public void actualizarVentanaActualiza(IDEventos idEvento, Object datos){
	    
    	if ( IDEventos.EVENTO_MODIFICAR_ARTICULO == idEvento ) {
			
			if ( datos instanceof Boolean ) {
				
				Boolean correcto = (Boolean) datos;
				
				if (correcto) {
					
					
					nombreAc.setText("");
					nombreAc.setEditable(false);
					valorAc.setText("");
					valorAc.setEditable(false);
					cantidadAc.setValue(0);
					cantidadAc.setEnabled(true);
					
					
					JFrame frame = new JFrame();
					JOptionPane.showMessageDialog(frame, "Articulo modificado", "Aviso", JOptionPane.INFORMATION_MESSAGE);
				}
				else {
					
					JFrame frame = new JFrame();
					JOptionPane.showMessageDialog(frame, "El articulo no se ha modificado", "Error", JOptionPane.ERROR_MESSAGE);
				}
				idArticulo = null;
			}
		}
		else if ( IDEventos.EVENTO_CONSULTAR_ARTICULO_MODIFICAR == idEvento ) {
			
			if ( datos instanceof TransferArticulo) {
				
				TransferArticulo articulo = (TransferArticulo) datos;
					
				idArticulo = articulo.getID();
				nombreAc.setText(articulo.getNombre());
				nombreAc.setEditable(true);
				valorAc.setText(articulo.getValor().toString());
				valorAc.setEditable(true);
				cantidadAc.setValue(articulo.getCantidad());
				cantidadAc.setEnabled(true);
				actualizaAc.setEnabled(true);
			}
		}
		else if ( IDEventos.ERROR_EVENTO_MODIFICAR_ARTICULO == idEvento || IDEventos.ERROR_EVENTO_CONSULTAR_ARTICULO_MODIFICAR == idEvento ) {
			
			if ( datos instanceof ExcepcionSQL ) {
				
				JFrame frame = new JFrame();
	    		JOptionPane.showMessageDialog(frame,((ExcepcionSQL)datos).getMensaje(), "Error", JOptionPane.ERROR_MESSAGE);
			}
			else {
				JFrame frame = new JFrame();
	    		JOptionPane.showMessageDialog(frame,"Error genérico", "Error", JOptionPane.ERROR_MESSAGE);
			}
		}
    }
    
