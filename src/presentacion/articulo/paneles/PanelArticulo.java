    private void enviarAActionPerformed(java.awt.event.ActionEvent evt) {                                        
        
    	TransferArticulo articulo = new TransferArticulo(); 
    
    	if ( !nombreA.getText().equals("") 
				&& !valorA.getText().equals("")
				&& cantidadA.getValue()!=aux){
    		
    		articulo.setNombre(nombreA.getText());
    		articulo.setValor(Float.parseFloat(valorA.getText()));
    		articulo.setCantidad((int) cantidadA.getValue());

    		Controlador.getInstance().handleRequest(IDEventos.EVENTO_ALTA_ARTICULO, articulo);
    	}
    	else
    	{
    		JFrame frame = new JFrame();
    		JOptionPane.showMessageDialog(frame,"Tienes que rellenar todos los campos", "Aviso", JOptionPane.WARNING_MESSAGE);
    	}
    	
    }                                       

                                           
